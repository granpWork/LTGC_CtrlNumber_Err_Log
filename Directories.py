class Directories:
    def __init__(self):
        self._inPath = ''
        self._outPath = ''

    def get_inPath(self):
        return self._inPath

    def set_inPath(self, a):
        self._inPath = a

    def get_outPath(self):
        return self._outPath

    def set_outPath(self, b):
        self._outPath = b


